public class ifElseOutput {
    public static void main(String[] args) {
        int numItems;

        numItems = 4;

        if (numItems < 3) {
            System.out.println("b");
        }

        if (numItems < 8) {
            System.out.println("f");
        }

        if (numItems == 4) {
            System.out.println("k");
        }

        System.out.println("r");
    }
}